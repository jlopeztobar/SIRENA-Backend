/**
 * Enumerado que representa los diferentes estados de una reserva.
 * Puede ser utilizado para indicar si una reserva está pendiente de aprobación,
 * ha sido aceptada o ha sido rechazada.
 */
public enum BookingStateTypeEnum {
    Pendiente,
    Aceptada,
    Rechazada,
    Finalizada
}
